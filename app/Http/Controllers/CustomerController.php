    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //Validate
        $this->validate($request, [
            'name' => 'required',
            'age' => 'required',
            'email' => 'required',
            'phone' => 'required',
        ]);

        //Save
        $myCustomer = new Customer();
        $myCustomer->name = $request->name;
        $myCustomer->email = $request->email;
        $myCustomer->documentid = $request->documentid;
        $myCustomer->phone = $request->phone;
        $myCustomer->age = $request->age;

        $myCustomer->save();

        return redirect()->route('customer.show', $myCustomer);

    }

